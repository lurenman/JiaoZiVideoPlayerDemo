public class RecyclerEntity {
    private String videoUrls;
    private String videoTitles;
    private String videoThumbs;

    public String getVideoUrls() {
        return videoUrls;
    }

    public void setVideoUrls(String videoUrls) {
        this.videoUrls = videoUrls;
    }

    public String getVideoTitles() {
        return videoTitles;
    }

    public void setVideoTitles(String videoTitles) {
        this.videoTitles = videoTitles;
    }

    public String getVideoThumbs() {
        return videoThumbs;
    }

    public void setVideoThumbs(String videoThumbs) {
        this.videoThumbs = videoThumbs;
    }
}
